let a = 10;
let b = 20;

// Arithmetic operators

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a++);
console.log(--b);

// Assignment operators

console.log(a += a);
console.log(a -= a);
console.log(b *= b);
console.log(b /= b);

// Comparison and Logical operator

console.log(a === 10 && b === 20);
console.log(a != 12 || b !== 22);